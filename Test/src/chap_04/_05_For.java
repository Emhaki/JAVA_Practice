package chap_04;

public class _05_For {
    public static void main(String[] args)
    {
        // 반복문
        System.out.println("어서오세요, 이명학입니다.");
        for (int i = 0; i < 10; i++) {
            System.out.println("안녕하세요 ㅎㅎ" + i);
        }
        for (int i = 5; i > 0; i--) {
            System.out.println(i);
        }
    }
}
