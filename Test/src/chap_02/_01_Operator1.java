package chap_02;

public class _01_Operator1 {
    public static void main(String[] args) {
        // 산술 연산자

        // 일반 연산
        System.out.println(4 + 2);
        System.out.println(4 * 2);
        System.out.println(4 / 2);
        System.out.println(2 / 4);

        // 변수를 이용한 연산
        int a = 10;
        int b = 20;
        int c;

        c = a + b;
        System.out.println(c);
    }
}